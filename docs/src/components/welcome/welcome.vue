    <h1>Welcome to CA11!</h1>
    <p>

        CA11 is a free softphone and decentralized public telephone network that
        enables people to receive and make peer-to-peer calls, video calls and
        screen sharing sessions. <img class="illustration right" src="/img/illustration-1.jpg"/>
        All you need is a web browser!
        Call privacy is protected with <a href="https://tools.ietf.org/html/rfc5764">standards-based</a>
        encryption technology and WebRTC offers high quality
        <a href="https://www.webmproject.org/vp9/">video</a> and
        <a href="http://opus-codec.org/">audio</a>.<br/><br/>

        You can use CA11 also to receive and make calls on a VoIP network,
        if you have an account from a compatible VoIP provider. Calls on a
        VoIP network are <em>not</em> end-to-end encrypted, but have the
        benefit of making you reachable on a broader range of phone
        networks. Checkout the <router-link to="/users/requirements">requirements</router-link>
        to to get started!
    </p>
