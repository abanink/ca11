    <h2>Earbuds/Headset</h2>
    <p>
        <img class="illustration right shadow" src="/screens/alice-wizard-devices.png"/>
        Audio calls require only a microphone. Most audio problems are
        caused by incorrect audio settings and bad microphone quality.
        The microphone on your laptop or webcam may sound to your caller,
        as if you're talking from out of a bathtub. For optimal communication
        with your callee, it is important to use a decent microphone!<br/><br/>

        Bluetooth earbuds/headsets give you space to move while you're on a call,
        but please notice that Bluetooth headset configuration could lead to more
        configuration issues. USB headsets have less room for error. USB and Bluetooth
        headsets will both show up as separate device in {{app.name}}'s device configuration.
        Headsets or earbuds with a Jack use the standard audio device on your computer.
    </p>

