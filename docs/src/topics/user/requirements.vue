    <h1>Requirements</h1>
    <h2>Platform</h2>
    <p>
        CA11 is flexible web-based software and runs as Electron desktop app,
        WebExtension and PWA(Progressive Web App). The <a href="https://ca11.io">CA11 PWA</a>
        seems at this point the most promising direction, because it covers the broadest
        amount of platforms, while it doesn't have to deal with vendor store
        distribution politics. The PWA can be installed on Android, Linux,
        Windows, OSX and Chromebooks. CA11 Electron and CA11 WebExtension are at
        this point not supported.
    </p>

    <h2>Headset</h2>
    <p>
        <img class="illustration" src="/screens/alice-wizard-devices.png"/>
        The microphone of your laptop or webcam may be sufficient for most
        use-cases, but for good audio experience, it is important to use a
        high quality headset with good ergonomics and a decent microphone.
        There are roughly 3 types of headsets: Bluetooth, USB and Jack.

        Bluetooth headsets gives you space to move while on a call and
        will work just fine in most cases, but it will introduce more
        user-related audio issues.<br/><br/>

        While there is not a list of supported headsets, it is generally
        save to say that any decent USB headset should work. USB headsets
        - just like Bluetooth headsets - will turn up as a separate device
        in CA11's device configuration. Headsets with a Jack are also

        suitable, but they don't show up as separate device
        in the list.




        has a separate device settings screen that shows up during
        the wizard, which you can use to verify your audio settings. The same
        device settings can be found in Settings.
    </p>


    <h3>A webcam</h3>
    <p>
        To make video calls, you need to have a webcam installed on your computer.
    </p>

